      <h2>Inspiration</h2>
      <p>After trying to stay current on US politics by listening to entire the Republican Convention in Cleveland, it struck me 
that a lot of the speakers were covering the same points using very similar language. I had heard of using <a href="https://en.wikipedia.org/wiki/Markov_chain">Markov chains</a> to generate text from a source text, and thought I could use it to make a plausible sounding speech for the convention. After mucking around for a day or so, I found that making a 'plausible' speech is hard, but making an absurd parody of the convention speeches was easy. I also enjoy absurd humor, so this worked out well for me.
      <h2>Source</h2>
      <p>For the source of this site, including transcripts and planned remarks, see
